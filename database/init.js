// Транслит для email-шаблона
const MAP = {
  а:'a',б:'b',в:'v',г:'g',д:'d',е:'e',ё:'e',ж:'zh',з:'z',и:'i',й:'y',к:'k',л:'l',м:'m',н:'n',
  о:'o',п:'p',р:'r',с:'s',т:'t',у:'u',ф:'f',х:'kh',ц:'ts',ч:'ch',ш:'sh',щ:'shch',ъ:'',ы:'y',ь:'',
  э:'e',ю:'yu',я:'ya',
};
function translit(s) {
  return s.toLowerCase().split('').map(c => MAP[c] !== undefined ? MAP[c] : c).join('');
}
function emailFromFio(fio) {
  const [familia, imya, otch] = fio.split(' ');
  const f = translit(familia);
  const i = imya ? translit(imya).charAt(0) : '';
  const o = otch ? translit(otch).charAt(0) : '';
  return `${f}.${i}${o}@alfabank.ru`;
}

